const textNodes = document.querySelectorAll("[data-i18n]");
const placeholderNodes = document.querySelectorAll("[data-i18n-placeholder]");
const valueNodes = document.querySelectorAll("[data-i18n-value]");
const hrefNodes = document.querySelectorAll("[data-i18n-href]");
const langButtons = document.querySelectorAll(".lang-btn");
const form = document.querySelector("#leadForm");
const formMessage = document.querySelector("#formMessage");
const metaDescription = document.querySelector('meta[name="description"]');

function applyLanguage(lang) {
  const pack = translations[lang] || translations.it;

  textNodes.forEach((node) => {
    const key = node.dataset.i18n;
    if (pack[key]) {
      node.textContent = pack[key];
    }
  });

  placeholderNodes.forEach((node) => {
    const key = node.dataset.i18nPlaceholder;
    if (pack[key]) {
      node.setAttribute("placeholder", pack[key]);
    }
  });

  valueNodes.forEach((node) => {
    const key = node.dataset.i18nValue;
    if (pack[key]) {
      node.value = pack[key];
    }
  });

  hrefNodes.forEach((node) => {
    const key = node.dataset.i18nHref;
    if (pack[key]) {
      node.setAttribute("href", pack[key]);
    }
  });

  document.documentElement.lang = lang;
  document.documentElement.dir = lang === "ar" ? "rtl" : "ltr";
  document.title = pack.title;
  if (metaDescription) {
    metaDescription.setAttribute("content", pack.description);
  }

  langButtons.forEach((btn) => {
    btn.classList.toggle("active", btn.dataset.lang === lang);
  });

  if (formMessage) {
    formMessage.textContent = "";
  }

  localStorage.setItem("preferredLang", lang);
}

langButtons.forEach((btn) => {
  btn.addEventListener("click", () => applyLanguage(btn.dataset.lang));
});

const savedLang = localStorage.getItem("preferredLang");
applyLanguage(savedLang && translations[savedLang] ? savedLang : "it");

if (form) {
  form.addEventListener("submit", () => {
    const lang = document.documentElement.lang === "ar"
      ? "ar"
      : document.documentElement.lang === "en"
        ? "en"
        : "it";

    formMessage.textContent = translations[lang].formSuccess;
    formMessage.style.color = "#97d7cb";
  });
}

const reveals = document.querySelectorAll(".reveal");
const observer = new IntersectionObserver(
  (entries) => {
    entries.forEach((entry) => {
      if (entry.isIntersecting) {
        entry.target.classList.add("show");
        observer.unobserve(entry.target);
      }
    });
  },
  { threshold: 0.16 }
);

reveals.forEach((item, index) => {
  item.style.transitionDelay = `${Math.min(index * 35, 240)}ms`;
  observer.observe(item);
});
